package com.lesath.apps.controller.model;

public class ExtendSchedule {
	int deltaDays;
	
	public ExtendSchedule(int deltaDays) {
		this.deltaDays = deltaDays;
	}

	/**
	 * @return the deltaDays
	 */
	public int getDeltaDays() {
		return deltaDays;
	}

	/**
	 * @param deltaDays the deltaDays to set
	 */
	public void setDeltaDays(int deltaDays) {
		this.deltaDays = deltaDays;
	}
	
}
